#"\t" funtion is used to represent some whitespaces, "\n" funtion is use to print newline.
name="\t""palwasha is very smart\n"
print(name)

#lstrip function removes any leading whitespaceson from left in the string.
name="   palwasha "
name.lstrip()
print(name)

#rstrip function removes any leading trailing whitespaces from the right in string.
name="palwasha     "
name.rstrip()
print(name)

#strip function is used to  removes any leading or trailing whitespaces from both left and right in string.
name="  Palwasha   "
name.strip()
print(name)