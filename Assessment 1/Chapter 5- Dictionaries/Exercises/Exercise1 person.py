# create a dictionary to store information about a person.
person={'first_name':'Anaya','last_name':'khan','age':15, 'city':'Dubai'}

# we will print each piece of information stored in the dictionary using the keys.
# The person['first_name] which will accesses the value associated with the ('first_name') key in the dictionary.
print("First Name:",person['first_name'])
# and so we will use for other keys as following. this will retrieve and display each piecenof information about the person.
print("Last Name:",person['last_name'])
print("Age:",person['age'])
print("City:",person['city'])